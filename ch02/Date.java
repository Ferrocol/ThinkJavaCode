public class Date {

  public static void main(String[] args) {
    String day = "Wednesday";
    String date = "2";
    String month = "August";
    String year = "2017";
    System.out.println("American format:");
    System.out.println(day + ", " + month + " " + date + ", " + year);
    System.out.println("European format:");
    System.out.println(day + " " + date + " " + month + " " + year);
  }
}
